// var

var myVar = 5;
var myVar = 12345;

console.log(`myVar = ${myVar}`); // myVar = 12345

// let 

let myLet = 5;
let myLet = 12345;

console.log(`myLet = ${myLet}`); 
// SyntaxError: Identifier 'myLet' has already been declared

// const

const myConst = 5;
const myConst = 12345;

console.log(`myConst = ${myConst}`);
// SyntaxError: Identifier 'myConst' has already been declared
